// Comments demo in JavaScript

console.log(10+10); // This will log 20 to the console
console.log(10*5);

console.log("Hello, World!"); // This will log "Hello, World!" to the console

// This is a single-line comment explaining the next line of code
console.log("This is a comment example."); // Comments can be used to explain code

/* This is a multi-line comment.
    It can span multiple lines and is useful for longer explanations.  
    It will not affect the code execution.
*/   

console.log("Comments are ignored by the JavaScript engine."); // Comments do not affect the code execution
console.log("You can use comments to make your code more readable."); // Use comments to clarify your code

// Remember to use comments wisely, they should not clutter your code
console.log("Good comments explain why something is done, not just what is done."); // Good comments provide context
console.log("Use comments to document complex logic or important decisions."); // Documenting complex logic is crucial
console.log("Avoid obvious comments that do not add value."); // Avoid redundant comments that do not help understanding
console.log("Use comments to leave notes for yourself or other developers."); // Leaving notes can be helpful
